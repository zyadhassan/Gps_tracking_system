#include"stdint.h"
#include"C:/Keil/EE319Kware/inc/tm4c123gh6pm.h"
#define RED 0x02
#define BLUE 0x04
#define GREEN 0x08

void SystemInit(){};
void init(){
uint32_t delay;
SYSCTL_RCGCGPIO_R |=0x20 ;
delay=1;
GPIO_PORTF_LOCK_R=0x4C4F434B;
GPIO_PORTF_CR_R=0x1F;
GPIO_PORTF_AFSEL_R=0;
GPIO_PORTF_PCTL_R=0;
GPIO_PORTF_AMSEL_R=0;
GPIO_PORTF_DIR_R=0x0E;
GPIO_PORTF_DEN_R=0x1F;
GPIO_PORTF_DATA_R&=0x00;




}
int main(){

init();
	while(1){
	if((GPIO_PORTF_DATA_R&0x11)==0x010)
		{ //LED 1 IS ON WHEN PIN 1 IS ON WHICH MEAN PIN 0 IS 0
	GPIO_PORTF_DATA_R=RED;
			GPIO_PORTF_DATA_R=0x00;
	}
		
	else if((GPIO_PORTF_DATA_R&0x11)==0x01) //LED 2 IS ON WHEN PIN 4 IS ON WHICH MEAN PIN IS ZERO
	{
	GPIO_PORTF_DATA_R=BLUE;
		GPIO_PORTF_DATA_R=0x00;
	
	}



	
	else if ((GPIO_PORTF_DATA_R&0x11)==0x00)//LED 3 IS ON WHEN PIN 0&4 IS OFF
	{
	
	(GPIO_PORTF_DATA_R=GREEN);
		GPIO_PORTF_DATA_R=0x00;
	
	}
	
	else
	//	LEDS ARE OFF WHEN PIN 0 AND 4 ARE OFF
	
		(GPIO_PORTF_DATA_R=0x00);

	
	
	
	}

}


